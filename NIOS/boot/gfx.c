{
  alt_16 f     =  1 - r;
  alt_16 ddF_x =  1;
  alt_16 ddF_y = -2 * r;
  alt_16 x     =  0;
  alt_16 y     =  r;

  while (x<y) {
    if (f >= 0) {
      y--;
      ddF_y += 2;
      f     += ddF_y;
    }
    x++;
    ddF_x += 2;
    f     += ddF_x;

    if (cornername & 0x1) {
      writeVLine(x0+x, y0-y, 2*y+1+delta, color);
      writeVLine(x0+y, y0-x, 2*x+1+delta, color);
    }
    if (cornername & 0x2) {
      writeVLine(x0-x, y0-y, 2*y+1+delta, color);
      writeVLine(x0-y, y0-x, 2*x+1+delta, color);
    }
  }
  return 0;
}

